weather = 'sunny'
case weather
  when 'sunny' then  puts "Its sunny so don't forget to wear sunscreen"
  when 'cloudy' then puts "Bring a sweater with you it might get chilly"
  when 'foggy' then puts "Be sure to wear a jacket, its foggy"
  when 'rainy' then puts "It is raining so don't forget your raincoat"
  else 
    puts "I don't know what you should wear in that kind of weather"
end 